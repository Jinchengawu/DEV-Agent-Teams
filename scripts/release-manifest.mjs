#!/usr/bin/env node
import { existsSync, mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import { join, resolve } from 'node:path';
import { execFileSync } from 'node:child_process';
import { fileURLToPath } from 'node:url';

const root = resolve(fileURLToPath(new URL('..', import.meta.url)));
const outputDir = join(root, 'release-manifests');
mkdirSync(outputDir, { recursive: true });

function readJson(path) {
  return JSON.parse(readFileSync(path, 'utf8'));
}

function git(args) {
  try {
    return execFileSync('git', args, { cwd: root, encoding: 'utf8' }).trim();
  } catch {
    return '';
  }
}

function packageInfo(path) {
  const pkg = readJson(join(root, path, 'package.json'));
  return {
    name: pkg.name,
    version: pkg.version || '0.0.0',
    private: Boolean(pkg.private),
    main: pkg.main || null,
    dependencies: pkg.dependencies || {},
  };
}

const rootPackage = readJson(join(root, 'package.json'));
const packages = [
  'packages/core',
  'packages/gateway',
  'packages/dashboard',
  'packages/glue-service',
].filter((path) => existsSync(join(root, path, 'package.json'))).map(packageInfo);

const manifest = {
  product: rootPackage.name,
  version: rootPackage.version,
  generatedAt: new Date().toISOString(),
  git: {
    branch: git(['rev-parse', '--abbrev-ref', 'HEAD']),
    commit: git(['rev-parse', 'HEAD']),
    shortCommit: git(['rev-parse', '--short', 'HEAD']),
    remoteMain: git(['rev-parse', '--short', 'origin/main']),
    dirty: git(['status', '--porcelain=v1']) !== '',
  },
  frameworkDependency: packages.find((pkg) => pkg.name === '@dev-agent/core')?.dependencies?.['@open-agent-teams/core'] || null,
  deployables: [
    {
      id: 'gateway',
      package: '@dev-agent/gateway',
      command: 'cd packages/gateway && node dist/api-gateway.js',
      portEnv: 'GATEWAY_PORT',
      defaultPort: 8400,
      readiness: '/health',
    },
    {
      id: 'dashboard',
      package: '@dev-agent/dashboard',
      command: 'cd packages/dashboard && next start',
      portEnv: 'PORT',
      defaultPort: 3000,
      readiness: '/api/health',
    },
  ],
  artifactClasses: [
    {
      id: 'coordination-data',
      description: 'Projects, kanban tasks, documents, workflows, sessions, and pipeline instances generated by the Team Coordination Loop.',
      localPaths: ['~/.dev-agent/data', '~/.dev-agent/logs'],
      retention: 'Keep across product upgrades; migrate with schema versions.',
    },
    {
      id: 'delivery-gate-reports',
      description: 'Closed-loop validation reports produced by scripts/e2e-delivery-gate.sh.',
      localPaths: ['scripts/test-reports'],
      retention: 'Commit only curated reports; archive noisy local runs before release.',
    },
    {
      id: 'codex-backfill-artifacts',
      description: 'Artifacts generated without external model calls while MODEL_SPEND_GUARD is enabled.',
      localPaths: ['~/.dev-agent/data/pipelines', '~/.dev-agent/data/*.db'],
      retention: 'Must include generatedBy=codex, executionMode=codex-backfill, externalModelTokens=0.',
    },
  ],
  packages,
};

const filename = `release-manifest-${new Date().toISOString().replace(/[:.]/g, '-')}.json`;
const outputPath = join(outputDir, filename);
writeFileSync(outputPath, `${JSON.stringify(manifest, null, 2)}\n`);
console.log(outputPath);
